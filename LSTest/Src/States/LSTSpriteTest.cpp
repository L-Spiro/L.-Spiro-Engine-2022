	// vTemp0.z = NEAR.
	// vTemp1.z = FAR.

	//CGfx::Clear();
	m_sspSprite->Orientation().SetPosU( CVector2( _pgGame->GetHalfWidthF(), _pgGame->GetHalfHeightF() ) );
	//m_sspSprite->Orientation().SetRotInDegrees( _pgGame->RenderTime().GetCurMills() / 10.0f );
	m_sspSprite2->Orientation().SetRotInDegrees( _pgGame->RenderTime().GetCurMills() / 150.0f );
